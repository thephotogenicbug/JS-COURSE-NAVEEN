let number1 = 5 // variable
let number2 = 20


// Increment (++)
console.log(++number1);
console.log(number1);

// Decrement (--)
console.log(--number1);
console.log(number1);

console.log(number1 + number2, "------------addition"); // expression
console.log(number1 - number2, "------------subtraction");
console.log(number1 * number2, "------------multiplication");
console.log(number1 / number2, "------------division");
console.log(number1 % number2, "------------percentage");
console.log(number1 ** number2, "------------exponent"); // number1 power of number 2