// Strict Equality Operator, same (Datatype + Value)
console.log(1 === 1); // Number === Number
console.log("1" === 1); // String === Number
console.log("--------------------------------------")
// Lose Eqaulity Operator
console.log(1 == 1); // Number == Number
console.log("1" == 1); // String == Number , 1st value is important
console.log(true == "1"); 
